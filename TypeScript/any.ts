#!/usr/bin/env ts-node
let myVariable: any;
// myVariable is “any” type
myVariable = 100;
// myVariable is “number” type
console.log(myVariable);
myVariable = "Scala in 8 Hours";
// myVariable is “string” type
console.log(myVariable);
myVariable = false;
// myVariable is “Boolean” type
console.log(myVariable);
